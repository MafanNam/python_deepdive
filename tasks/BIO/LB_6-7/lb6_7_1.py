import numpy as np


def global_alignment(seq1, seq2, subst_matrix, gap_penalty):
    # Determine the lengths of the sequences
    len_seq1, len_seq2 = len(seq1), len(seq2)

    # Initialize score and traceback matrices
    score_matrix = np.zeros((len_seq1 + 1, len_seq2 + 1))
    traceback_matrix = np.zeros((len_seq1 + 1, len_seq2 + 1))

    # Fill the first row and column of the score matrix with gap penalties
    for i in range(1, len_seq1 + 1):
        score_matrix[i, 0] = score_matrix[i - 1, 0] + gap_penalty
    for j in range(1, len_seq2 + 1):
        score_matrix[0, j] = score_matrix[0, j - 1] + gap_penalty

    # Fill the score and traceback matrices
    for i in range(1, len_seq1 + 1):
        for j in range(1, len_seq2 + 1):
            match_score = score_matrix[i - 1, j - 1] + subst_matrix[seq1[i - 1]][seq2[j - 1]]
            delete_score = score_matrix[i - 1, j] + gap_penalty
            insert_score = score_matrix[i, j - 1] + gap_penalty
            score_matrix[i, j] = max(match_score, delete_score, insert_score)
            traceback_matrix[i, j] = 1 if score_matrix[i, j] == match_score else (
                2 if score_matrix[i, j] == delete_score else 3)

    # Traceback to construct aligned sequences
    aligned_seq1, aligned_seq2 = [], []
    i, j = len_seq1, len_seq2
    while i > 0 or j > 0:
        if traceback_matrix[i, j] == 1:
            aligned_seq1.append(seq1[i - 1])
            aligned_seq2.append(seq2[j - 1])
            i -= 1
            j -= 1
        elif traceback_matrix[i, j] == 2:
            aligned_seq1.append(seq1[i - 1])
            aligned_seq2.append('-')
            i -= 1
        else:
            aligned_seq1.append('-')
            aligned_seq2.append(seq2[j - 1])
            j -= 1

    # Reverse aligned sequences to correct order
    aligned_seq1.reverse()
    aligned_seq2.reverse()

    # Return aligned sequences and alignment score
    return ''.join(aligned_seq1), ''.join(aligned_seq2), score_matrix[len_seq1, len_seq2]


